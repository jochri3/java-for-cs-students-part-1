import java.util.Arrays;
 
 
public class Program18DynamicArrays {
    public static void main(String[] args) {
        int[] intsArray=new int[5];//0
        double[] decimalsArray=new double[5];//0.0
        boolean[] booleansArray=new boolean[5];//false
        String[] stringsArray=new String[5];//null
 
        System.out.println(Arrays.toString(intsArray));
        System.out.println(Arrays.toString(decimalsArray));
        System.out.println(Arrays.toString(booleansArray));
        System.out.println(Arrays.toString(stringsArray));
    }
}